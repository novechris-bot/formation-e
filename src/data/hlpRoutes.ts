export interface RouteStep {
  direction: string;
  details?: string;
}

export interface HLPRoute {
  id: string;
  category: 'sortie' | 'rentree' | 'inter-ligne';
  depot: string;
  origin: string;
  destination: string;
  itinerary: RouteStep[];
  notes?: string;
  lineInfo?: string;
}

// Fonction pour parser un itinéraire en étapes avec directions
export function parseItinerary(itineraryText: string): RouteStep[] {
  const segments = itineraryText.split(' - ').filter(s => s.trim());
  return segments.map(segment => ({
    direction: segment.trim(),
  }));
}

