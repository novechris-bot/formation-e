// Obtenir toutes les destinations uniques
export function getUniqueDestinations(): string[] {
  const destinations = new Set<string>();
  hlpRoutes.forEach(route => {
    destinations.add(route.destination);
    destinations.add(route.origin);
  });
  return Array.from(destinations).sort((a, b) => a.localeCompare(b, 'fr'));
}

// Obtenir tous les points de départ uniques
export function getUniqueOrigins(): string[] {
  const origins = new Set<string>();
  hlpRoutes.forEach(route => {
    origins.add(route.origin);
  });
  return Array.from(origins).sort((a, b) => a.localeCompare(b, 'fr'));
}

// Obtenir tous les dépôts uniques
export function getUniqueDepots(): string[] {
  const depots = new Set<string>();
  hlpRoutes.forEach(route => {
    if (route.depot) {
      depots.add(route.depot);
    }
  });
  return Array.from(depots).sort((a, b) => a.localeCompare(b, 'fr'));
}

// Rechercher des parcours
export function searchRoutes(origin?: string, destination?: string, category?: string): HLPRoute[] {
  return hlpRoutes.filter(route => {
    const matchOrigin = !origin || route.origin.toLowerCase().includes(origin.toLowerCase());
    const matchDestination = !destination || route.destination.toLowerCase().includes(destination.toLowerCase());
    const matchCategory = !category || route.category === category;
    return matchOrigin && matchDestination && matchCategory;
  });
}

// Trouver un parcours exact
export function findExactRoute(origin: string, destination: string): HLPRoute | undefined {
  return hlpRoutes.find(route => 
    route.origin === origin && route.destination === destination
  );
}

// Obtenir les destinations disponibles depuis un point de départ
export function getDestinationsFromOrigin(origin: string): string[] {
  const destinations = new Set<string>();
  hlpRoutes.forEach(route => {
    if (route.origin === origin) {
      destinations.add(route.destination);
    }
  });
  return Array.from(destinations).sort((a, b) => a.localeCompare(b, 'fr'));
}

